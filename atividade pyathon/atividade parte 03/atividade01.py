# ATIVIDADE 1: Crie um loop que conte de 1 a 10 e mostre cada número
# while passando uma condição para repetir
# contador vai ser o contador de quantas repeticoes iremos fazer
contador = 1
# while significa enquanto
while contador <=10:
    # printando o contador para mostrar de 1 até 10 pois o contador irá contar estes 10 números
    print(contador)
    contador = contador + 1
print("Finalizou")
